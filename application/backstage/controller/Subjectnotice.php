namespace app\backstage\controller;

use library\Controller;
use think\Db;
use think\Request;

/**
 * 考试须知
 * Class Subjectnotice
 * @package app\backstage\controller
 */
class Subjectnotice extends Controller
{

    /**
     * 指定当前数据表
     * @var string
     */
    public $table = 'XmSubjectNotice';

    public function __construct(){
        parent::__construct();
    }

    /**
     * 分类列表管理
     * @auth true
     * @menu true
     * @throws \think\Exception
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     */
    public function index()
    {
        $this->title = '考试须知';
        $query = $this->_query($this->table)->like('content');
        $query->dateBetween('create_at')->where(['is_deleted' => '0'])->order('id desc')->page();
    }

    /**
     * 列表数据处理
     * @auth true
     * @param array $data
     * @throws \Exception
     */
    protected function _index_page_filter(&$data)
    {
        foreach($data as &$vo){
            $vo['snum']  = Db::name('XmSubjectNotice')->where(['is_deleted' => '0','id'=>$vo['id']])->count();
        }
    }

    /**
     * 添加分类信息
     * @auth true
     * @throws \think\Exception
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     * @throws \think\exception\PDOException
     */
    public function add()
    {
        $this->applyCsrfToken();
        $this->_form($this->table, 'form');
    }
    /**
     * 表单数据处理
     * @param array $data
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     */
    public function _form_filter(&$data)
    {
        if ($this->request->isPost()) {

           $data['create_at'] = date('Y-m-d H:i:s');

        }
    }

    /**
     * 编辑分类信息
     * @auth true
     * @throws \think\Exception
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     * @throws \think\exception\PDOException
     */
    public function edit()
    {
        $this->applyCsrfToken();
        $this->_form($this->table, 'form');
    }

    /**
     * 删除分类
     * @auth true
     * @throws \think\Exception
     * @throws \think\exception\PDOException
     */
    public function remove()
    {
        $this->applyCsrfToken();
        $this->_delete($this->table);
    }

}
